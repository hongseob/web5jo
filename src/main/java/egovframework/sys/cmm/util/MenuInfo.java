package egovframework.sys.cmm.util;

public class MenuInfo {
	 
	
	String mainmenu; // 메인메뉴
	String submainmenu; // 서브메뉴에 속한 메인메뉴
	String submenu; // 서브메뉴
	String newname; // 새로운 이름
	String icon; // 아이콘 
	int id; // 아이디
	

	public String getMainmenu() {
		return mainmenu;
	}
	public void setMainmenu(String mainmenu) {
		this.mainmenu = mainmenu;
	}
	public String getSubmenu() {
		return submenu;
	}
	public void setSubmenu(String submenu) {
		this.submenu = submenu;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNewname() {
		return newname;
	}
	public void setNewname(String newname) {
		this.newname = newname;
	}
	public String getSubmainmenu() {
		return submainmenu;
	}
	public void setSubmainmenu(String submainmenu) {
		this.submainmenu = submainmenu;
	}
	
	
	
	
        
}
